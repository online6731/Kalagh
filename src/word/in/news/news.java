package word.in.news;

public class news {
    String title;
    String link;
    String description;
    String author;
    String date;
    int number;

    news(String title, String link, String description, String author, String date, int number) {
        this.title = title;
        this.link = link;
        this.description = description;
        this.author = author;
        this.date = date;
        this.number = number;
    }
}
